/* ── Language detection & application ──────────────────── */

const SUPPORTED_LANGS = Object.keys(TRANSLATIONS);
const STORAGE_KEY     = "luxe_nails_lang";

/**
 * Detect language:
 *  1. localStorage preference
 *  2. navigator.language / navigator.languages
 *  3. Fallback to 'en'
 */
function detectLang() {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored && SUPPORTED_LANGS.includes(stored)) return stored;

  const navLangs = navigator.languages || [navigator.language || "en"];
  for (const lang of navLangs) {
    const code = lang.split("-")[0].toLowerCase();
    if (SUPPORTED_LANGS.includes(code)) return code;
  }
  return "en";
}

/**
 * Apply all [data-i18n] bindings for the given lang code.
 * Also updates <html lang>, <title>, and meta description.
 */
function applyLang(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS["en"];

  // Update html[lang]
  document.documentElement.lang = lang;

  // Update <title> and meta description
  document.title = t.page_title;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.content = t.page_desc;

  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Mark active lang button
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang ? "true" : "false");
  });

  // Save preference
  localStorage.setItem(STORAGE_KEY, lang);

  // Re-render dynamic sections
  renderServices(lang);
  renderTestimonials(lang);
}

/* ── Render services grid ───────────────────────────────── */
function renderServices(lang) {
  const t    = TRANSLATIONS[lang] || TRANSLATIONS["en"];
  const grid = document.getElementById("servicesGrid");
  if (!grid) return;

  grid.innerHTML = t.services.map(s => `
    <article class="service-card reveal" role="listitem">
      <div class="service-icon" aria-hidden="true">${s.icon}</div>
      <h3 class="service-name">${s.name}</h3>
      <p  class="service-desc">${s.desc}</p>
      <span class="service-price">
        <i class="fa-solid fa-tag" aria-hidden="true"></i>
        ${s.price}
      </span>
    </article>
  `).join("");

  // Re-attach intersection observer to new cards
  observeReveal();
}

/* ── Render testimonials ────────────────────────────────── */
function renderTestimonials(lang) {
  const t    = TRANSLATIONS[lang] || TRANSLATIONS["en"];
  const grid = document.getElementById("testimonialsGrid");
  if (!grid) return;

  grid.innerHTML = t.testimonials.map(r => `
    <article class="testimonial-card reveal">
      <div class="testimonial-stars" aria-label="5 stars">★★★★★</div>
      <blockquote class="testimonial-text">"${r.text}"</blockquote>
      <div class="testimonial-author">
        <div class="author-avatar" aria-hidden="true">${r.initial}</div>
        <div>
          <p class="author-name">${r.author}</p>
          <p class="author-tag">${r.tag}</p>
        </div>
      </div>
    </article>
  `).join("");

  observeReveal();
}

/* ── Wire up lang switcher buttons ─────────────────────── */
document.addEventListener("DOMContentLoaded", () => {
  const lang = detectLang();
  applyLang(lang);

  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", () => applyLang(btn.dataset.lang));
  });
});
